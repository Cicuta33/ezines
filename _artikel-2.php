			<section>
				<p class="intro">Social media wordt steeds belangrijker in onze communicatie. De doelgroep van Agentschap NL maakt meer en meer gebruik van social media en ook de AgNL-medewerkers begeven zich steeds vaker binnen de wondere wereld van social media. Maar hoe zetten ze social media in voor het contact met de doelgroep?</p>
				<p>Rob Smit (@SmitRR), adviseur Energie Investeringsaftrek (EIA), zit puur vanuit zijn werk op social media. Op Twitter luister ik vooral. Ik volg bijvoorbeeld politici en provincies en luister naar wat er leeft en speelt. Ook heb ik een LinkedIn profiel en volg ik discussies in groepen die een link hebben met mijn aandachtsgebied energiebesparing en verduurzaming. Op ondernemersplatform Higherlevel.nl (van Antwoord voor bedrijven, onderdeel van AgNL, red.) heb ik eens gereageerd op een vraag van een ondernemer die een energiezuinig kantoor wilde laten bouwen. Op deze externe platformen geef ik heel weloverwogen een reactie en geef ik altijd aan dat ik bij Agentschap NL werk. Op Yammer bemoei ik me wat actiever, maar ik houd het wel puur zakelijk.</p>
				<blockquote class="left">
					<p>&#8220;Sommige filmpjes zijn wel 10.000 keer bekeken!&#8221;</p>
				</blockquote>
				<p>Daniel van Vulpen (@hollandtrade) is projectleider internationale media bij NL EVD internationaal: Vanuit het programma Hollandtrade twitteren we nu ongeveer een halfjaar. We zenden vooral nieuws- berichten van de site Hollandtrade. com en we 'retweeten' leuke succesverhalen van Nederlandse bedrijven in het buitenland. Maar ook gaven we na de aardbeving in Japan een blijk van medeleven via Twitter. Naast Twitter hebben we een YouTube kanaal waarop we filmpjes plaatsen van succesvolle producten uit Nederland, bijvoorbeeld de Superbus en rallyauto's die op biogas rijden. Mensen willen toch graag iets zien en wij willen minder drukwerk produceren. Het YouTube kanaal is een soort etalage van de BV Nederland.</p> 
